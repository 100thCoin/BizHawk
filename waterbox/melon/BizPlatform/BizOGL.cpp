void LoadGL(LoadGLProc load, LoadGLVersion version, bool isWinApi)
{
	switch (version)
	{
		case LoadGLVersion::V3_2:
			LoadGL3_2(load);
			break;
		case LoadGLVersion::V4_3:
			LoadGL4_3(load);
			break;
		default:
			__builtin_unreachable();
	}

	IsWinAPI = isWinApi;
}

